    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (isEmpty()) {
            if (!check()) {
                int id = Integer.parseInt(txtIdSupplier.getText());
                String username = txtSupplierName.getText();
                String email = txtEmail.getText();
                String password = String.valueOf(txtPassword.getPassword());
                String phone = txtPhone.getText();
                String address1 = txtAdd1.getText();
                String address2 = txtAdd2.getText();
                supplier.update(id, username, email, password, phone, address1, address2);
                TableManageSupplier.setModel(new DefaultTableModel(null, new Object[]{"ID người dùng", "Tên người dùng", "Email", "Mật khẩu", "Số điện thoại", "Địa chỉ 1", "Địa chỉ 2"}));
                supplier.getSupplierValue(TableManageSupplier, "");
                clear();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (txtIdSupplier.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn một nhà cung cấp!", "Cảnh báo", 2);
        } else {
            int id = Integer.valueOf(txtIdSupplier.getText());
            supplier.delete(id);
            TableManageSupplier.setModel(new DefaultTableModel(null, new Object[]{"ID người dùng", "Tên người dùng", "Email", "Mật khẩu", "Số điện thoại", "Địa chỉ 1", "Địa chỉ 2"}));
            supplier.getSupplierValue(TableManageSupplier, "");
            clear();
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private boolean check() {
        String newUserName = txtSupplierName.getText();
        String newEmail = txtEmail.getText();
        String newPhone = txtPhone.getText();
        String oldUserName = model.getValueAt(rowIndex, 1).toString();
        String oldEmail = model.getValueAt(rowIndex, 2).toString();
        String oldPhone = model.getValueAt(rowIndex, 4).toString();
        if (newUserName.equals(oldUserName) && newEmail.equals(oldEmail) && newPhone.equals(oldPhone)) {
            return false;
        } else {
            if (!newUserName.equals(oldUserName)) {
                boolean x = supplier.isUserNameExist(newUserName);
                if (x) {
                    JOptionPane.showMessageDialog(this, "Người dùng này đã tồn tại", "Cảnh báo", 2);
                }
                return x;
            }
            if (!newEmail.equals(oldEmail)) {
                boolean x = supplier.isEmailExist(newEmail);
                if (x) {
                    JOptionPane.showMessageDialog(this, "Email này đã tồn tại", "Cảnh báo", 2);
                }
                return x;
            }
            if (!newPhone.equals(oldPhone)) {
                boolean x = supplier.isPhoneExist(newPhone);
                if (x) {
                    JOptionPane.showMessageDialog(this, "Số điện thoại này đã tồn tại", "Cảnh báo", 2);
                }
                return x;
            }
        }
        return false;
    }

