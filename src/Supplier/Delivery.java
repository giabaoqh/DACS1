    private void purchaseTable() {
        purchaseDao.getOnTheWayProducts(TableDelivery, "", supplierName);
        model = (DefaultTableModel) TableDelivery.getModel();
        TableDelivery.setRowHeight(30);
        TableDelivery.setShowGrid(true);
        TableDelivery.setGridColor(Color.BLACK);
        TableDelivery.setBackground(Color.WHITE);
        TableDelivery.setSelectionBackground(Color.LIGHT_GRAY);
    }

