    private void TableDeliveryMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableDeliveryMouseClicked
        model = (DefaultTableModel) TableDelivery.getModel();
        rowIndex = TableDelivery.getSelectedRow();
        
        int id = Integer.parseInt(model.getValueAt(rowIndex, 0).toString());
        String receivedDate = df.format(date);
        String status = "Đã nhận";
        purchaseDao.setDateStatus(id, receivedDate, status);
        TableDelivery.setModel(new DefaultTableModel(null, new Object[]{"ID mua", "ID người dùng", "Tên người dùng",
            "Số điện thoại", "ID sản phẩm", "Tên sản phẩm", "Số lượng", "Giá", "Tổng", "Ngày mua", "Địa chỉ", "Ngày nhận",
            "Tên nhà cung cấp", "Trạng thái"}));
        purchaseDao.getOnTheWayProducts(TableDelivery, "", supplierName);
        statistics.supplier(supplierDao.getSupplierName(SupplierDashboard.supplierEmail.getText()));
    }//GEN-LAST:event_TableDeliveryMouseClicked

